class Song:
    def setArtist(self, artist):
        self.artist=artist

    def getArtist(self):
        return self.artist

    def setTitle(self, title):
        self.title=title

    def getTitle(self):
        return self.title

    def setTime(self, time):
        self.time=time

    def getTime(self):
        return self.time

    def setYear(self, year):
        self.year=year

    def getYear(self):
        return self.year

    
